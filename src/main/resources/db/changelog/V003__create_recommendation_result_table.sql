CREATE TABLE recommendation_result (
   id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   recommendation_run_id BIGINT NOT NULL,
   vehicle_id BIGINT NOT NULL,
   rank INTEGER NOT NULL,
   match_reason TEXT NOT NULL,
   tradeoffs_json JSONB NOT NULL,
   facts_used_json JSONB NOT NULL,
   created_at TIMESTAMP WITHOUT TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
   CONSTRAINT fk_recommendation_result_run
       FOREIGN KEY (recommendation_run_id)
           REFERENCES recommendation_run (id)
           ON DELETE CASCADE,
   CONSTRAINT uq_recommendation_result_run_rank
       UNIQUE (recommendation_run_id, rank)
);